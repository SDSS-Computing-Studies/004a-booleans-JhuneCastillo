#! python3
# Have the user input a number 
# Determine if the number is positive, negative or 0 
# 2 points

# Inputs:
# number

# Outputs:
# - "positive"
# - "negative"
# - "zero"

x = input("Enter a number: ")
x = int(x)
x2 = 0

if (x == x2):
    print("zero")

if (x > x2):
    print("positive")

if (x < x2):
    print("negative")
